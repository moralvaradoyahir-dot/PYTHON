# calcular el area y el perimetro del triangulo equilatero

base = float(input("por favor, ingresa el valor de un lado del triangulo equilatero "))

altura = float(input("por favor, ingresa el valor de la altura del triangulo equilatero "))


print("El perimetro del triangulo equilatero es " + input(base*3))

print("El area del triangulo equilatero es " + input(base*altura/2))
